const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const routeSchema = mongoose.Schema({
    areaName : {
        type : String,
        require : true
    },
    is_active : {
        type : String,
        require : true,
        enum: ['YES','NO'],
        default: 'YES'
    },
    is_delete : {
        type : String,
        require : true,
        enum: ['YES','NO'],
        default: 'NO'
    },
    createdBy : {
        type: Schema.ObjectId,
        ref:'User'
    },
    updatedBy : {
        type: Schema.ObjectId,
        ref:'User'
    }
}, 
{ 
    timestamps: {} 
});


const Route = module.exports = mongoose.model('Route',routeSchema);